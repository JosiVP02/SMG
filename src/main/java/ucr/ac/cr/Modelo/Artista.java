package ucr.ac.cr.Modelo;

/**
 *
 * @author Usuario
 */
public class Artista 
{
    private String nombre;
    private String pais;
    private String genero;
    private int id;

    public Artista(String nombre, String pais, String genero, int id) 
    {
        this.nombre = nombre;
        this.pais = pais;
        this.genero = genero;
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
    
    
    
}
